package Ex_002;

public class PrintFormat {
    public static void main(String[] args) {

        int a = 5;
        char b = 'K';
        String c = "Nitin";
        float d = 3.14f;
        boolean e = false;

        System.out.printf("This is integer: %d%n", a);
        System.out.printf("This is Character: %c%n", b);
        System.out.printf("This is string: %s%n", c);
        System.out.printf("This is float: %f%n", d);
        System.out.printf("This is boolean: %b", e);

    }
}
