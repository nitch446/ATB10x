package Ex_003_Concatenation;

public class Concatenate_Char_ASCII {
    public static void main(String[] args) {
        char a ='A';  // ASCII -> 65
        char b ='B';  // ASCII -> 66
        System.out.println(a+b);   // Output => 65+66 = 131
        System.out.println('A'+'B');  // Output => 65+66 = 131
    }
}
